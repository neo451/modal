local getScale
getScale = function(name, num)
  num = tonumber(num)
  local scale = scaleTable[name]
  local index = (num + 1) % #scale
  local octave = math.floor((num) / (#scale))
  if index == 0 then
    index = #scale
  end
  local note = scale[index] + octave * 12
  return note
end
return {
  scaleTable = scaleTable,
  getScale = getScale
}
